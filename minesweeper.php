    <h1>The JavaScript Minesweeper Story</h1>
    <h2>Goal</h2>
    It's pretty straight forward: Get my hands dirty playing with Canvas and rendering with HTML5. So we use canvas graphics to make the images. No PNG or GIf or JPG anywhere.
    Also it's important to make this touch friendly as a priority.  This means changing the game slightly and removing the flags and question mark tools that help you navigate the
    mine field. 
